DATA SEGMENT
	MSG1 DB 13,10, "ENTER FIRST NUMBER (0-99): $"
	MSG2 DB 13,10, "ENTER FIRST NUMBER (0-99): $"
	MSGRES DB 13,10, "RESULT OF MULTIPLICATION: $"
	RES DB '00', '$'
	RES1 DB '00','$'
DATA ENDS

STACK SEGMENT STACK
	DW 128 DUP(?)
STACK ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA, SS:STACK

START:
	MOV AX,DATA
	MOV DS,AX
	
	LEA DX,MSG1
	MOV AH,09H
	INT 21H
	CALL GET2DIGIT
	MOV BL,CL
	MOV BH,CH
	
	LEA DX,MSG2
	MOV AH,09H
	INT 21H
	CALL GET2DIGIT
	
	MOV AL,CL
	MUL BL
	MOV AH,0
	CALL ADJRES
	
	ADD AL,'0'
	MOV RES1[1],AL
	MOV DH,AH
	
	MOV AL,CL
	MUL BH
	ADD AL,DH
	MOV AH,0
	CALL ADJRES
	MOV DL,AL
	MOV DH,AH
	
	MOV AL,CH
	MUL BL
	ADD AL,DL
	MOV AH,0
	CALL ADJRES
	ADD AL,'0'
	MOV RES1[0],AL
	MOV DL,AH
	
	MOV AL,CH
	MUL BL
	ADD AL,DL
	ADD AL,DH
	MOV AH,0
	CALL ADJRES
	ADD AL,'0'
	MOV RES[1],AL
	ADD AH,'0'
	MOV RES[0],AH
	
	CALL DISPRES
	
	MOV AH,4CH
	INT 21H
	
GET2DIGIT:
	MOV AH,01H
	INT 21H
	MOV CH,AL
	MOV AH,01H
	INT 21H
	MOV CL,AL
	SUB CL,'0'
	SUB CH,'0'
	RET

ADJRES:
	CMP AL,9
	JBE ADJRET
	SUB AL,10
	INC AH
	JA ADJRES
ADJRET:
	RET
	
DISPRES:
	LEA DX,MSGRES
	MOV AH,09H
	INT 21H
	LEA DX,RES
	MOV AH,09H
	INT 21H
	LEA DX, RES1
	MOV AH, 09H
	INT 21H
	RET

CODE ENDS
END START
