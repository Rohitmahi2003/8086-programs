DATA SEGMENT
	STR1 DB 30 DUP("$")
	STR2 DB 30 DUP("$")
	MSG1 DB 13,10,"STR 1:$"
	MSG2 DB 13,10,"STR 2:$"
	RES DB 13,10,"CONCATENATED STRING: $"
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:
	MOV AX,DATA
	MOV DS,AX
	MOV CL,04
	LEA DX,MSG1
	MOV AH,09H
	INT 21H
	LEA SI,STR1
	LOOP0: MOV AH,01H
		INT 21H
		CMP AL,13
		JE SKIP0
		MOV [SI],AL
		INC SI
		JMP LOOP0
	SKIP0: MOV AH,09H
		LEA DX,MSG2
		INT 21H
        LEA SI,STR2
	LOOP1: MOV AH,01H
		INT 21H
		CMP AL,13
		JE SKIP1
		MOV [SI],AL
		INC SI
		JMP LOOP1
	SKIP1: CALL CONCAT
		MOV AH,4CH
		INT 21H
CONCAT PROC
	LEA SI,STR1
	LEA DI,STR2
	MOV AL,"$"
	LOOP2: CMP AL,[SI]
		JZ LOOP3
		INC SI
		JMP LOOP2
	LOOP3:CMP AL,[DI]
		JZ EXIT
		MOV BL,[DI]
		MOV [SI],BL
		INC SI
		INC DI
		JMP LOOP3
	EXIT: MOV [DI],AL
		LEA DX,RES
		MOV AH,09H
		INT 21H
		LEA DX,STR1
		MOV AH,09H
		INT 21H
		RET
CONCAT ENDP
CODE ENDS
END START
