.MODEL SMALL
.DATA
	MSG1 DB 13,10,"NUM 1: $"
	MSG2 DB 13,10,"NUM 2: $"
        PROD DB 13,10,"PROD: $"
	TEN DW 10
.CODE
.STACK
START:
	MOV AX,@DATA
	MOV DS,AX
	LEA DX,MSG1
	MOV AH,09H
	INT 21H
	CALL INPUT
	MOV BH,DL
	LEA DX,MSG2
	MOV AH,09H
	INT 21H
	CALL INPUT 
	MOV BL,DL
	MOV AL,DL
	MUL BH
	MOV BX,AX
	MOV AH,09H
	LEA DX,PROD
	INT 21H
	CALL DEC_OUTPUT
	MOV AH,4CH
	INT 21H
INPUT PROC
	CALL INPUTNUM
	MOV CL,04H
	ROL DL,CL
	AND DL,00F0H
	MOV CH,DL
	CALL INPUTNUM
	AND DL,000FH
        ADD DL,CH
	RET
INPUT ENDP
INPUTNUM PROC
	MOV AH,01H
	INT 21H
	CMP AL,'A'
	JC INPUTNUM_IS_DEC
	JMP INPUTNUM_IS_HEX
	INPUTNUM_IS_HEX:
	SUB AL,'A'
	ADD AL,0AH
	MOV DL,AL
	RET
	INPUTNUM_IS_DEC:
	SUB AL,'0'
	MOV DL,AL
	RET
	INPUTNUM ENDP
DEC_OUTPUT PROC
	MOV CX,0
	OUTPUT_LOOP:
	MOV AX,BX
	MOV DX,0
	DIV WORD PTR TEN
	PUSH DX
	INC CX
	MOV BX,AX
	CMP BX,0
	JNZ OUTPUT_LOOP
DISPLAY_DIGITS:
	POP DX
	ADD DL,'0'
	MOV AH,02H
	INT 21H
	LOOP DISPLAY_DIGITS
	RET
DEC_OUTPUT ENDP
END START
